package com.palminput.keyboard.skin;

import android.graphics.Paint;

public class SkinButtonPaint {

    private Paint paintBg;
    private Paint paintFg;
    private Paint paintBorder;
    private Paint paintShadow;

    public Paint getPaintBg() {
        return paintBg;
    }

    public void setPaintBg(Paint paintBg) {
        this.paintBg = paintBg;
    }

    public Paint getPaintFg() {
        return paintFg;
    }

    public void setPaintFg(Paint paintFg) {
        this.paintFg = paintFg;
    }

    public Paint getPaintBorder() {
        return paintBorder;
    }

    public void setPaintBorder(Paint paintBorder) {
        this.paintBorder = paintBorder;
    }

    public Paint getPaintShadow() {
        return paintShadow;
    }

    public void setPaintShadow(Paint paintShadow) {
        this.paintShadow = paintShadow;
    }
}
